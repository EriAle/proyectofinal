    public void actualizarArticulos(){
        String[] columnNames = {"clave","precio","descripcion", "tipo"};
        Articulo[] articulo = conex.guardarArticulo();
        dtArticulos = new Object[articulo.length][4];
        for (int i = 0 ;i < articulo.length; i++ ){
            dtArticulos[i][0] = articulo[i].getClave();
            dtArticulos[i][1] = articulo[i].getPrecio();
            dtArticulos[i][2] = articulo[i].getDescripcion();
            dtArticulos[i][3] = articulo[i].getTipo();
        }
        tPrincipal = new JTable(dtArticulos,columnNames);
    }
